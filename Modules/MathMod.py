
def div(num1, num2):
    """ divides two numbers """
    return num1/num2

def fdiv(num1,num2):
    """ floor division of two numbers """
    return num1 // num2

def add(*argv):
    """ add n numbers """
    return sum(argv)

def avg(*argv):
    """ returns the average of numbers """
    return sum(argv) / len(argv)

